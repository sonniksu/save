package Day5_2;

public class Shirt {

	String product;
	String color;
	String company;
	String material;
	String size;
	int price;
	
	
	public Shirt(String product, String color, String company, String material, String size, int price) {
		this.product= product;
		this.color = color;
		this.company = company;
		this.material = material;
		this.size=size;
		this.price=price;
	}
}
