package Day5_2;

public class Book {

	String category;
	String title;
	String writer;
	String publisher;
	int price;
	String number;
	
	
	public Book(String category, String title, String writer, String publisher, int price, String number ) {
		this.category = category;
		this.title = title;
		this.writer = writer;
		this.publisher = publisher;
		this.price=price;
		this.number=number;
	
			
	}
}
